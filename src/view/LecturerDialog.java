    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String name = JOptionPane.showInputDialog(this, "Nama Dosen:");
        if (name != null && !name.trim().isEmpty()) {
            int userId = Session.getUser().getId();
            Lecturer l = new Lecturer(0, userId, name.trim());
            if (lecturerDAO.add(l)) {
                JOptionPane.showMessageDialog(this, "Berhasil menambahkan!");
                loadLecturers();
                if (getParent() instanceof MainFrame) {
                    ((MainFrame) getParent()).refreshLecturerCombo();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambahkan.");
            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = tbLecturer.getSelectedRow();
        if (row != -1) {
            int id = (int) tbLecturer.getValueAt(row, 0);
            String currentName = (String) tbLecturer.getValueAt(row, 1);
            String newName = JOptionPane.showInputDialog(this, "Edit Nama Dosen:", currentName);
            if (newName != null && !newName.trim().isEmpty()) {
                int userId = Session.getUser().getId();
                Lecturer l = new Lecturer(id, userId, newName.trim());
                if (lecturerDAO.update(l)) {
                    JOptionPane.showMessageDialog(this, "Berhasil diperbarui!");
                    loadLecturers();
                    if (getParent() instanceof MainFrame) {
                        ((MainFrame) getParent()).refreshLecturerCombo();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal diperbarui.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris dulu!");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = tbLecturer.getSelectedRow();
        if (row != -1) {
            int id = (int) tbLecturer.getValueAt(row, 0);
            int confirm = JOptionPane.showConfirmDialog(this, "Yakin mau hapus?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                int userId = Session.getUser().getId();
                if (lecturerDAO.delete(id, userId)) {
                    JOptionPane.showMessageDialog(this, "Berhasil dihapus!");
                    loadLecturers();
                    if (getParent() instanceof MainFrame) {
                        ((MainFrame) getParent()).refreshLecturerCombo();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal dihapus.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris dulu!");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

