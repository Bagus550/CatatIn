    private void loadLecturers() {
        int userId = Session.getUser().getId();

        List<Lecturer> list = lecturerDAO.getAll(userId);
        DefaultTableModel model = new DefaultTableModel(
            new Object[]{"ID", "Nama Dosen"}, 0
        );

        for (Lecturer l : list) {
            model.addRow(new Object[]{l.getId(), l.getName()});
        }

        tbLecturer.setModel(model);
        TableStyler.styleTable(tbLecturer);
        hideIdColumn(tbLecturer);
    }


    private void hideIdColumn(JTable table) {
        table.getColumnModel().getColumn(0).setMinWidth(0);
        table.getColumnModel().getColumn(0).setMaxWidth(0);
        table.getColumnModel().getColumn(0).setWidth(0);
        table.getColumnModel().getColumn(0).setPreferredWidth(0);
    }

