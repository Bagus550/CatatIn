    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = tbCourse.getSelectedRow();
    if (row != -1) {

        int id = (int) tbCourse.getValueAt(row, 0);
        String currentName = (String) tbCourse.getValueAt(row, 1);

        String newName = JOptionPane.showInputDialog(
                this,
                "Edit Nama Mata Kuliah:",
                currentName
        );

        if (newName != null && !newName.trim().isEmpty()) {

            Course c = new Course();
            c.setId(id);
            c.setUserId(Session.getUser().getId());
            c.setName(newName.trim());
            c.setLecturerId(0);

            if (courseDAO.update(c)) {
                JOptionPane.showMessageDialog(this, "Berhasil diperbarui!");
                loadCourses();

                if (getParent() instanceof MainFrame) {
                    ((MainFrame) getParent()).refreshCourseCombo();
                }

            } else {
                JOptionPane.showMessageDialog(this, "Gagal diperbarui.");
            }
        }

    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris dulu!");
    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String name = JOptionPane.showInputDialog(this, "Nama Mata Kuliah:");
            if (name != null && !name.trim().isEmpty()) {
                Course c = new Course();
                c.setUserId(Session.getUser().getId());
                c.setName(name.trim());
                c.setLecturerId(0); // default dulu

                if (courseDAO.add(c)) {
                    JOptionPane.showMessageDialog(this, "Berhasil menambahkan!");
                    loadCourses();
                    if (mainFrame != null) mainFrame.refreshCourseCombo();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menambahkan.");
                }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = tbCourse.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris dulu!");
            return;
        }

        int id = (int) tbCourse.getValueAt(row, 0);
        int userId = Session.getUser().getId();

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Yakin mau hapus?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        if (courseDAO.delete(id, userId)) {
            JOptionPane.showMessageDialog(this, "Berhasil dihapus!");
            loadCourses();

            if (getParent() instanceof MainFrame) {
                ((MainFrame) getParent()).refreshCourseCombo();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Gagal dihapus.");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

