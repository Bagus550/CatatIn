    private void loadCourses() {
        List<Course> list = courseDAO.getAll(Session.getUser().getId());
        DefaultTableModel model = new DefaultTableModel(new Object[]{"ID", "Nama Mata Kuliah"}, 0);
        for (Course c : list) {
            model.addRow(new Object[]{c.getId(), c.getName()});
        }
        tbCourse.setModel(model);
        TableStyler.styleTable(tbCourse);
        hideIdColumn(tbCourse);
    }


    private void hideIdColumn(JTable table) {
        table.getColumnModel().getColumn(0).setMinWidth(0);
        table.getColumnModel().getColumn(0).setMaxWidth(0);
        table.getColumnModel().getColumn(0).setWidth(0);
        table.getColumnModel().getColumn(0).setPreferredWidth(0);
    }
    
